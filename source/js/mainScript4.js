$('#1Quest').on('click', function(e)
{
    e.preventDefault();
    var data = $(this).serialize();
    $.ajax
    ({
        url: './pages/Round_4/1Quest.html', 
        type: 'GET',
        success: function(responce)
        {        
            $('#ShowPage').html(responce); 
        },
        error: function()
        {
            alert('Error!');            
        }
    });
});

